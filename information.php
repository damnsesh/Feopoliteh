      <div class="information__p">
      <p>     Феодосийский политехнический техникум основан в 1966 г. Приказом Министерства черной металлургии УССР №305 от 15.07.66г.<br>
           15.03.1996г. исполнительным комитетом Феодосийского городского совета Автономной Республики Крым было зарегистрировано Крымское республиканское высшее учебное заведение «Феодосийский политехнический техникум».<br>
           Во исполнение положений Федерального конституционного закона от 21 марта 2014 года №6-ФКЗ «О принятии в Российскую Федерацию новых субъектов – Республики Крым и города федерального значения Севастополя», приказа Министерства образования, науки и молодежи Республики Крым от 19 ноября 2014 года №287 «О приведении учредительных документов учебных заведений и научных организаций в соответствие с законодательством Российской Федерации и включении сведений в Единый государственный реестр юридических лиц» принято решение о включении Государственного бюджетного профессионального образовательного учреждения Республики Крым «Феодосийский политехнический техникум» в Единый государственный реестр юридических лиц.
</p>

<span class="read__more1" onClick="ShowText()">     Читать далее</span>

      <p class="hidden-txt">
           Учредителем ГБ ПОУ РК «ФПТ» и собственником его имущества является Республика Крым. Функции и полномочия Учредителя ГБ ПОУ РК «ФПТ» в соответствии с законодательством Российской Федерации и законодательством Республики Крым осуществляет Министерство образования, науки и молодежи Республики Крым.

      <br>     Функции и полномочия Учредителя Учреждения в соответствии с законодательством Российской Федерации и законодательством Республики Крым осуществляет Министерство образования, науки и молодежи Республики Крым.

      <br>     За годы существования техникум подготовил более 10 тыс. специалистов разных специальностей, которые работают на всей территории СНГ.

      <br>     Техникум готовит специалистов среднего звена по специальностям:

<em><br>     15.02.16 «Технология машиностроения»,
<br>     38.02.01 «Экономика и бухгалтерский учет»,
<br>     19.02.10 «Технология продукции общественного питания»,
<br>     43.02.15 «Поварское и кондитерское дело»,
<br>     08.02.09 «Монтаж, наладка и эксплуатация электрооборудования промышленных и гражданских зданий»,
<br>     09.02.07 «Информационные системы и программирование»,
<br>     40.02.01 «Право и организация социального обеспечения».
</em>
<br>     Материальная база техникума составляет 44 учебных аудитории, которые оснащены необходимыми наглядными пособиями и оборудованием. Кроме этого есть спортивный и актовый залы, библиотека с читальным залом и столовая. Иногородние студенты могут проживать в общежитии, которое расположено на берегу моря. Студенты имеют возможность проводить свое свободное время, занимаясь в секциях и в кружках художественной самодеятельности студенческого клуба «Территория».

<br>     Самым ценным в техникуме является его педагогический состав, который дает твердые, фундаментальные знания своим студентам. В составе педагогических кадров большое количество молодых преподавателей, закончившие ВУЗы Крыма, Украины и России. Все преподаватели техникума профессионалы своего дела. Многие преподаватели техникума за свою работу награждены Почетными грамотами Министерства образования и науки России и Крыма.
      </p>

